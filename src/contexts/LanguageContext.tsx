interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string) => string;
  isBilingual: boolean;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

const SUPPORTED_LANGUAGES: Language[] = ['fr', 'en', 'ar', 'es', 'pt', 'zh', 'tr'];
const LANG_STORAGE_KEY = 'gocivique-lang';

function isSupported(lang: string | null | undefined): lang is Language {
  return !!lang && (SUPPORTED_LANGUAGES as string[]).includes(lang);
}

/** Resolve initial language: ?lang= URL param → saved preference → fr (always default) */
function detectInitialLanguage(): Language {
  if (typeof window === 'undefined') return 'fr';
  try {
    const urlLang = new URLSearchParams(window.location.search).get('lang');
    if (isSupported(urlLang)) return urlLang;
    const stored = localStorage.getItem(LANG_STORAGE_KEY);
    if (isSupported(stored)) return stored;
  } catch {
    // ignore (storage blocked, etc.)
  }
  return 'fr';
}

function applyDocumentLanguage(lang: Language) {
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
}

/** Keep ?lang= in the URL in sync so links are shareable and canonical/hreflang stay consistent. */
function syncUrlLangParam(lang: Language) {
  try {
    const url = new URL(window.location.href);
    if (lang === 'fr') {
      url.searchParams.delete('lang');
    } else {
      url.searchParams.set('lang', lang);
    }
    window.history.replaceState(window.history.state, '', url.toString());
  } catch {
    // ignore
  }
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>(detectInitialLanguage);

  // Apply lang/dir for the detected language on first mount (not only on manual switch)
  useEffect(() => {
    applyDocumentLanguage(language);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  const t = (key: string): string => {
    return translations[language]?.[key] || translations.fr[key] || key;
  };

  const isBilingual = language !== 'fr';

  const handleSetLanguage = (lang: Language) => {
    setLanguage(lang);
    applyDocumentLanguage(lang);
    syncUrlLangParam(lang);
    try {
      localStorage.setItem(LANG_STORAGE_KEY, lang);
    } catch {
      // ignore
    }
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage: handleSetLanguage, t, isBilingual }}>
      {children}
    </LanguageContext.Provider>
  );
}

const fallback: LanguageContextType = {
  language: 'fr',
  setLanguage: () => { },
  t: (key: string) => translations.fr[key] || key,
  isBilingual: false,
};

export function useLanguage() {
  const context = useContext(LanguageContext);
  return context ?? fallback;
}